# a classe é um conjunto de def(funções)
# the class is a set of def (functions)

class person:
    def show_name(name):
        print(name)
    def show_age(age):
        print(age)

class pessoa:
    def mostrar_nome(nome):
        print(nome)
    def mostrar_idade(idade):
        print(idade)


person.show_name('James')
person.show_age(27)

pessoa.mostrar_nome('Arthur')
pessoa.mostrar_idade(17)