number = 0
numero = 0


# while criará uma repetição (while em inglês significa enquanto) logo precisará de uma comparação para ser realizado, e apra parar usa-se um break no fim
# while will create a repetition it will soon need a comparison to be performed, and to stop it a break is used at the end


while number <= 10:
    print(number)
    number = number + 1

while number <= 10:
    print(number)
    break



while numero <= 10:
    print(numero)
    numero = numero + 1

while numero <= 10:
    print(numero)
    break

